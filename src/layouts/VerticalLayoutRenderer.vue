<template>
  <v-container v-if="layout.visible" fill-height :class="styles.verticalLayout.root">
    <v-row
      v-for="(element, index) in layoutUiSchema.elements"
      :key="`${layout.path}-${index}`" no-gutters
    >
      <v-col cols="12" :class="styles.verticalLayout.item">
        <dispatch-renderer
          :schema="layout.schema"
          :uischema="element"
          :path="layout.path"
          :enabled="layout.enabled"
          :renderers="layout.renderers"
          :cells="layout.cells"
        />
      </v-col>
    </v-row>
  </v-container>
</template>

<script lang="ts">
import {
  uiTypeIs,
  JsonFormsRendererRegistryEntry,
  Layout,
  VerticalLayout,
  rankWith,
} from "@jsonforms/core";
import { defineComponent } from "../../config/vue";
import {
  DispatchRenderer,
  rendererProps,
  useJsonFormsLayout,
  RendererProps,
} from "../../config/jsonforms";
import { useVuetifyLayout } from "../util";
import { VContainer, VRow, VCol } from "vuetify/lib";

const layoutRenderer = defineComponent({
  name: "vertical-layout-renderer",
  components: {
    DispatchRenderer,
    VContainer,
    VRow,
    VCol,
  },
  props: {
    ...rendererProps<Layout>(),
  },
  setup(props: RendererProps<Layout>) {
    return useVuetifyLayout(useJsonFormsLayout(props));
  },
  computed: {
    layoutUiSchema(): VerticalLayout {
      return this.layout.uischema as VerticalLayout;
    },
  },
});

export default layoutRenderer;

export const entry: JsonFormsRendererRegistryEntry = {
  renderer: layoutRenderer,
  tester: rankWith(2, uiTypeIs("VerticalLayout")),
};
</script>
